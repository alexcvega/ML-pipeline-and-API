# --- 1. Data Auditing & Cleaning ---
df = pd.read_csv('healthcare-dataset-stroke-data.csv')

# Audit: Drop 'id' and the single 'Other' gender row to ensure stability
df.drop(columns=['id'], inplace=True)
df = df[df['gender'] != 'Other']

# Audit: Inconsistencies & Outliers
# We keep high glucose outliers because they are clinically significant for health predictions.
df.drop_duplicates(inplace=True)

# Define Target: Predicting 'bmi' (Continuous)
# We drop rows where BMI is NaN for the training/testing ground truth
df_reg = df.dropna(subset=['bmi'])
X = df_reg.drop(columns=['bmi', 'stroke'])
y = df_reg['bmi']
import seaborn as sns
