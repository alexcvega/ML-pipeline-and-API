from fastapi import FastAPI
import joblib
import pandas as pd
from pydantic import BaseModel

# Load the full serialized pipeline
model_pipeline = joblib.load('bmi_prediction_pipeline.pkl')

app = FastAPI(title="Stroke Data BMI Predictor")

# Define schema for validation
class PatientInput(BaseModel):
    gender: str
    age: float
    hypertension: int
    heart_disease: int
    ever_married: str
    work_type: str
    Residence_type: str
    avg_glucose_level: float
    smoking_status: str

@app.post("/predict")
def predict(data: PatientInput):
    # Convert incoming JSON to DataFrame
    input_df = pd.DataFrame([data.model_dump()])

    # Predict using the loaded pipeline (handles scaling/encoding automatically)
    prediction = model_pipeline.predict(input_df)

    return {
        "predicted_bmi": round(float(prediction[0]), 2),
        "input_summary": f"Patient Age: {data.age}, Glucose: {data.avg_glucose_level}"
    }